@posix_only
def test_write_config_chmod_600(tmp_path: Path) -> None:
    config_path = tmp_path / "xzssh.json"
    cfg = Config(version=1, hosts=[Host(alias="db", host_name="db.example.com")])

    write_config(config_path, cfg)

    mode = stat.S_IMODE(config_path.stat().st_mode)
    assert mode == 0o600, f"expected 0o600, got {oct(mode)}"


@posix_only
def test_generate_chmod_600_on_output(tmp_path: Path) -> None:
    config_path = tmp_path / "xzssh.json"
    output_path = tmp_path / "ssh_config"
    _seed_xzssh_config(config_path)

    main(
        [
            "generate",
            "--config",
            str(config_path),
            "--output",
            str(output_path),
        ]
    )

    mode = stat.S_IMODE(output_path.stat().st_mode)
    assert mode == 0o600, f"expected 0o600, got {oct(mode)}"


def test_write_config_leaves_no_tmp_file_behind(tmp_path: Path) -> None:
    config_path = tmp_path / "xzssh.json"
    cfg = Config(version=1, hosts=[Host(alias="db", host_name="db.example.com")])

    write_config(config_path, cfg)

    siblings = list(tmp_path.iterdir())
    assert config_path in siblings
    assert all(not s.name.endswith(".tmp") for s in siblings), siblings
